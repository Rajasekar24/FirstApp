package com.oopsoverloading;

public class PersonalDetails {

	int age;
	String name;
	boolean offerletter, payslip, bankStatement, panCard;
	
	public PersonalDetails(int age, String name, boolean offerletter, boolean payslip, boolean bankStatement,
			boolean panCard) {
		super();
		this.age = age;
		this.name = name;
		this.offerletter = offerletter;
		this.payslip = payslip;
		this.bankStatement = bankStatement;
		this.panCard = panCard;
	} 
	
	
}
